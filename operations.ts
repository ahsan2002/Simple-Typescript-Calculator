const sum = (a: number, b: number): number => {
    let result: number;
    result = a + b;
    return result
}

const sub = (a: number, b: number): number => {
    let result: number;
    result = a - b;
    return result
}

const mul = (a: number, b: number): number => {
    let result: number;
    result = a * b;
    return result
}

const div = (a: number, b: number): number => {
    let result: number;
    result = a / b;
    return result
}


export { sum, sub, mul, div };