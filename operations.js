const sum = (a, b) => {
    let result;
    result = a + b;
    return result;
};
const sub = (a, b) => {
    let result;
    result = a - b;
    return result;
};
const mul = (a, b) => {
    let result;
    result = a * b;
    return result;
};
const div = (a, b) => {
    let result;
    result = a / b;
    return result;
};
export { sum, sub, mul, div };
